def seed():
    Base.metadata.create_all(bind=engine)
    db = SessionLocal()
    vs = create_vector_store(settings)

    existing = db.query(Fragment).count()
    if existing > 0:
        print(f"Already {existing} fragments exist. Skipping seed.")
        db.close()
        return

    for data in SAMPLE_FRAGMENTS:
        frag = Fragment(**data)
        db.add(frag)
        db.flush()

        text = "\n".join(
            p for p in [frag.title, frag.summary, frag.detail, frag.lesson] if p
        )
        vs.upsert(frag.id, text, {"tags": ",".join(frag.tags)})

    db.commit()
    print(f"Seeded {len(SAMPLE_FRAGMENTS)} fragments.")
    db.close()


if __name__ == "__main__":
    seed()
